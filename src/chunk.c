Chunk *criar_chunks(int indice){
    Chunk *c = malloc(sizeof(Chunk));
    c->proximo = NULL;

    for(int l=0; l<CHUNK_ROWS; l++){
        for(int co=0; co<CHUNK_COLS; co++){
            char chunk = chunks[indice][l][co];

            if(chunk == '#'){
                c->mapa[l][co] = 1;
            }

            else if(chunk == 'X'){
                c->mapa[l][co] = 2;
            }

            else{
                c->mapa[l][co] = 0;
            }
        }
    }

    return c;
}

void destruir_chunk(Chunk *c){
    free(c);
}